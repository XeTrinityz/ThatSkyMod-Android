            ("kMask", 0x7C4, "const char* at [outfit + 0x7C4]"),
            ("kNeck", 0x9F4, "const char* at [outfit + 0x9F4]"),
            ("kBody", 0x134, "const char* at [outfit + 0x134]"),
            ("kFeet", 0xC24, "const char* at [outfit + 0xC24]"),
