def get_image_base():
    """Get the image base address"""
    return idaapi.get_imagebase()

def pattern_to_bytes(pattern_str):
    """
    Convert pattern string (e.g., "48 89 5C 24 ?? 48 89 74") to bytes and mask
    
    Returns:
        tuple: (bytes, mask_bytes) where mask_bytes uses 0xFF for match, 0x00 for wildcard
    """
    parts = pattern_str.split()
    pattern_bytes = []
    mask_bytes = []
    
    for part in parts:
        if part == "??":
            pattern_bytes.append(0)
            mask_bytes.append(0x00)  # Wildcard
        else:
            pattern_bytes.append(int(part, 16))
            mask_bytes.append(0xFF)  # Must match
    
    return bytes(pattern_bytes), bytes(mask_bytes)

def find_pattern(pattern_str, start_ea=None, end_ea=None):
    """
    Search for a pattern in the binary
    
    Args:
        pattern_str: Pattern string (e.g., "48 89 5C 24 ?? 48")
        start_ea: Start address (None = start of segment)
        end_ea: End address (None = end of segment)
    
    Returns:
        list: List of addresses where pattern was found
    """
    if start_ea is None:
        start_ea = ida_segment.get_first_seg().start_ea
    if end_ea is None:
        end_ea = ida_segment.get_last_seg().end_ea
    
    results = []
    current_ea = start_ea
    
    # Use find_bytes which is a higher-level wrapper
    while current_ea < end_ea:
        found_ea = ida_bytes.find_bytes(
            pattern_str,
            current_ea,
            end_ea,
            16,  # radix (hex)
            ida_bytes.BIN_SEARCH_FORWARD | ida_bytes.BIN_SEARCH_NOSHOW
        )
        
        if found_ea == idaapi.BADADDR:
            break
        
        results.append(found_ea)
        current_ea = found_ea + 1
    
    return results

def scan_signature(sig_data):
    """
    Scan for a signature and return the offset
    
    Args:
        sig_data: Signature data dictionary
    
    Returns:
        int or None: Found offset (RVA) or None if not found
    """
    pattern = sig_data["pattern"]
    sig_type = sig_data["type"]
    pattern_offset = sig_data.get("pattern_offset", 0)
    
    # Search for pattern
    matches = find_pattern(pattern)
    
    # If no matches, try alternative pattern if available
    if not matches and "alt_pattern" in sig_data:
        print(f"    Trying alternative pattern...")
        matches = find_pattern(sig_data["alt_pattern"])
        if matches:
            pattern_offset = sig_data.get("alt_offset", 0)
    
    if not matches:
        return None
    
    if len(matches) > 1:
        print(f"    [!] Warning: Multiple matches found ({len(matches)}), using first")
    
    # Calculate the actual offset based on pattern_offset and type
    found_ea = matches[0]
    
    # Handle different signature types
    if sig_type == "data_xref":
        # For data xrefs, extract the data address from the instruction
        xref_insn_ea = found_ea + pattern_offset
        insn = idautils.DecodeInstruction(xref_insn_ea)
        if insn:
            # Look for memory operand (the data reference)
            data_found = False
            for op in insn.ops:
                if op.type == idaapi.o_mem:
                    # This is the data address
                    found_ea = op.addr
                    print(f"    Extracted data address: 0x{found_ea:X}")
                    data_found = True
                    break
            
            if not data_found:
                # Couldn't extract data address - return xref location with marker
                print(f"    [!] Could not extract data address from instruction")
                print(f"    [!] Returning xref instruction location: 0x{xref_insn_ea:X}")
                print(f"    [!] Manual resolution needed - check instruction at this address")
                # Return special marker value that we'll handle in write_header
                return ("XREF", xref_insn_ea - image_base)
        else:
            print(f"    [!] Warning: Could not decode instruction for data_xref")
            return None
    else:
        # Regular pattern - apply pattern_offset
        found_ea = found_ea + pattern_offset
    
    # Convert back to RVA (offset from image base)
    image_base = get_image_base()
    rva = found_ea - image_base
    
    return rva

def load_signatures(input_file):
    """Load signatures from JSON file"""
    if not os.path.exists(input_file):
        print(f"[!] Error: Signatures file not found: {input_file}")
        print("Please run ida_generate_signatures.py first!")
        return None
    
    with open(input_file, 'r') as f:
        return json.load(f)

def scan_all_signatures(signatures):
    """Scan all signatures and return updated offsets"""
    print(f"Scanning {len(signatures)} signatures...\n")
    
    found_offsets = OrderedDict()
    failed = []
    
    # First, populate with all original offsets from signatures
    for name, sig_data in signatures.items():
        found_offsets[name] = sig_data['offset']
    
    for name, sig_data in signatures.items():
        print(f"Scanning {name}...")
        print(f"  Original offset: 0x{sig_data['offset']:X}")
        
        try:
            new_offset = scan_signature(sig_data)
            
            if new_offset is not None:
                # Check if it's a special XREF marker tuple
                if isinstance(new_offset, tuple) and new_offset[0] == "XREF":
                    found_offsets[name] = new_offset
                    print(f"  ⚠ Found xref instruction at: 0x{new_offset[1]:X} (manual resolution needed)")
                else:
                    found_offsets[name] = new_offset
                    
                    if new_offset == sig_data['offset']:
                        print(f"  ✓ Found at: 0x{new_offset:X} (unchanged)")
                    else:
                        print(f"  ✓ Found at: 0x{new_offset:X} (moved from 0x{sig_data['offset']:X})")
            else:
                print(f"  [!] Not found!")
                failed.append(name)
                # Keep old offset as fallback (already set above)
                
        except Exception as e:
            print(f"  [!] Error: {e}")
            failed.append(name)
            # Keep old offset as fallback (already set above)
    
    print(f"\n{'='*80}")
    print(f"Found: {len(found_offsets) - len(failed)}/{len(signatures)}")
    if failed:
        print(f"Failed: {len(failed)} signatures")
        print(f"Failed offsets: {', '.join(failed)}")
    
    return found_offsets, failed

